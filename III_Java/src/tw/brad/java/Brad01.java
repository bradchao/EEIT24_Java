public class Brad01 {

	public static void main(String[] args) {
		// 整數 byte, short, int, long
		byte var1;	// 2^8 -128 ~ 127
					// [a-zA-Z$_][a-zA-Z0-9$_]*
		byte $$$ = 100;
		byte $_$ = 101;
		System.out.println($$$);
		System.out.println($_$);
		var1 = 123;
		System.out.println(var1);
		
		var1 = 127;
		System.out.println(var1);
		//var1 = -129;
		
		
	}

}
